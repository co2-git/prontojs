! function () {

  require('colors');

  module.exports = function () {
    
    return (function start () {

      var pronto = this;

      pronto.domain.run(function () {
        var middleware_position = 0;

        pronto.app
          .set('port', +pronto.port);

        pronto.app.use(function (req, res, next) {

          req.startTime = Date.now();

          req.suggestedType = 'html';

          if ( /\.css$/i.test(req.originalUrl) ) {
            req.suggestedType = 'css';
          }

          else if ( /\.js$/i.test(req.originalUrl) ) {
            req.suggestedType = 'js';
          }

          else if ( /\.jade$/i.test(req.originalUrl) ) {
            req.suggestedType = 'jade';
          }

          pronto.emitRequest(req);

          next();
        });

        pronto.middlewares = [
          {
            type: 'use',
            name: 'encode/decode URLs',
            middleware: require('body-parser').urlencoded({ extended: false })
          },

          {
            type: 'use',
            name: 'parse JSON',
            middleware: require('body-parser').json()
          },

          {
            type: 'use',
            name: 'Pronto pre-router',
            middleware: function prontoJS_preRouterMiddleware (req, res, next) {
              res.locals.req = req;

              try {
                res.locals.package = require(
                  require('path').join(process.cwd(), 'package.json'));
              }
              catch ( error ) {
                //
              }

              next();
            }
          }]
          
          .concat(pronto.middlewares)
          
          .concat([
            {
              type: 'use',
              name: 'error catcher',
              error: true,
              middleware: function prontoJS_errorMiddleware (error, req, res, next) {
                res.statusCode = 500;
                pronto.emitResponse(req, res);
                pronto.emitError(error);
              }
            },
            
            {
              type: 'use',
              name: 'not found',
              middleware: function prontoJS_notFoundMiddleware (req, res, next) {
                res.statusCode = 404;
                res.type(req.suggestedType);
                pronto.emitResponse(req, res);
                pronto.emit(404, req.originalUrl);

                var notFound = pronto.middlewares.filter(
                  function (middleware) {
                    if ( middleware.statuses && middleware.statuses.length ) {
                      return middleware.statuses.some(function (status) {
                        return status === 404;
                      });
                    }
                    return false;
                  });

                if ( notFound.length ) {
                  notFound.forEach(function (status404) {
                    status404.middleware(req, res);
                  });
                }

                else {
                  res.end('page not found');
                }
              }
            }]);

        // Inject each middleware into app

        pronto.middlewares.forEach(function (middleware) {

          var exp_app_method = pronto.app[middleware.type];
          var Hook = require('../Hook').bind(pronto);

          // If middleware has no routes filter

          if ( ! middleware.routes || ! middleware.routes.length ) {
            exp_app_method.apply(pronto.app, [Hook(middleware)]);
          }

          else {
            exp_app_method.apply(pronto.app,
              [middleware.routes].concat([Hook(middleware)]));
          }

        });

        // pronto.use(pronto.errorMiddleware());

        pronto.server = require('http').createServer(pronto.app);

        pronto.server.listen(pronto.port, function () {

          var stream = require('fs').createReadStream(require('path')
            .join(__dirname, '../../ascii.txt'));

          var ascii = '';

          stream.on('data', function (data) {
            ascii += data.toString();
          });

          stream.on('end', function () {
            ascii = ascii.yellow.bold;

            ascii = ascii.replace(/\$/g, '$'.bgBlack);

            console.log(ascii);

            pronto.emit('listening', {
              port: pronto.app.get('port'),
              protocol: 'http',
              pid: process.pid,
              env: pronto.app.get('env'),
              middlewares: middlewaresSummary(pronto.middlewares)
            });
          });

        });
      });

    });

    function middlewaresSummary (middlewares) {
      var summary = [];

      middlewares.forEach(function (middleware) {
        var middleware_summary = [ middleware.type, middleware.name ];

        if ( middleware.name === 'open file' ) {
          middleware_summary.push(middleware.options.file);
        }

        else if ( middleware.name === 'open directory' ) {
          middleware_summary.push(middleware.options.directory);

          if ( middleware.options.opener ) {
            for ( var i in middleware.options.opener ) {
              switch ( i ) {
                case 'as':
                  middleware_summary.push('as', middleware.options.opener.as);
                  break;

                case 'index':
                  middleware_summary.push('using as index', middleware.options.opener.index);
                  break;

                case 'append extension':
                  middleware_summary.push('appending extension', middleware.options.opener['append extension']);
                  break;

                case 'with':
                  middleware_summary.push('with', middleware.options.opener.with);
                  break;
              }
            }
          }
        }

        else if ( middleware.name === 'redirect' ) {
          middleware_summary.push('to', middleware.options.redirect);
        }

        if ( middleware.routes ) {
          middleware_summary.push('when', middleware.routes.join(' or '));
        }

        if ( middleware.cookies ) {
          middleware.cookies.forEach(function (cookie) {
            middleware_summary.push('has', (
              (cookie.signed ? 'signed cookie ' : 'cookie ') +
              cookie.name
            ));
          });
        }

        summary.push(middleware_summary.join(' '));
      });

      return summary;
    }

  }();

} ();
